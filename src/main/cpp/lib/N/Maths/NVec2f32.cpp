#include "lib/N/NCStandard.h"
#include "lib/N/Maths/NVec2f32.h"
#include "lib/N/NType.h"
// -------------------------------------------------------------------------------------------
// NVec2Normalize
// -------------------------------------------------------------------------------------------
//	Normalize a vector
// -------------------------------------------------------------------------------------------
// In  :
//		v: Vector pointer.
//
// Out :
//		Vector length before normalization.
//
// -------------------------------------------------------------------------------------------
Nf32 NVec2Normalize(NVEC2* v)
{
	Nf32	n,sn;

	n = sqrt(v->x * v->x + v->y * v->y);
	if( n >= NF32_EPSILON_VECTOR_LENGTH )
	{
		sn = 1.0f / n;
		v->x *= sn;
		v->y *= sn;
	}

	return n;
}

// -------------------------------------------------------------------------------------------
// NVec2FastNormalize
// -------------------------------------------------------------------------------------------
//	Normalize a vector
// -------------------------------------------------------------------------------------------
// In  :
//		v: Vector pointer.
//
// Out :
//		Vector length before normalization.
//
// -------------------------------------------------------------------------------------------
Nf32 NVec2FastNormalize(NVEC2* v)
{
	Nf32	n,sn;

	n = NFastSqrt(v->x * v->x + v->y * v->y);
	if( n >= NF32_EPSILON_VECTOR_LENGTH )
	{
		sn = 1.0f / n;
		v->x *= sn;
		v->y *= sn;
	}

	return n;
}
