// + This is not a .h file, but a documentation file about COMPILER DEFINE
// +
// + The N Engine compiler define are:
// + 
// +			_NLIBS				Adds some specific functionalities about libraries using. Like textures, blend and material libraries.
// +								Based on HashMaps these libraries simplify external resources management.
// +	
// +			_NEDITOR			All the code parts used by the InGame Editor. Has to be OFF for the final retail release version
// +								(to remove all the editor part) .
// +								It adds some specific engine functionalities used by editor ( tool management for example)
// +
// +			_NANDROID			All the code parts specific to the ANDROID platform
// +
// +			_NIOS				All the code parts specific to the IOS platform
// +
// +			_NWINDOWS			All the code parts specific to the WINDOWS platform
// +
// +			
// +			
// +			_NOUT_xxx			"Printf" messages activated. the second part of the define (xxx) hint about what kind of messages
// +								are going to be printed out.
// +			
// +			
// +			_NOUT_SYS_UTIL		"Printf" from "NUT_" functions	
// +			
// +			_NOUT_SYS_EVENT		"Printf" from "Event" management functions	
// +			
// +			
// +			_NUT_LOGGING		Activate the logging "defines" as "functions". 
// +								Without this define, 'NUT_Logging' or "__NUT_InitLogging" will be simply ignored at compilation.
// +			
